//write a program that reads 2 numbewrs and print their +,-,*,/ as following
#include<iostream>
using namespace std;
int main()
{
    double a,b;
    cin>> a >> b;
cout<<a<<"+"<<b<<"="<<a+b<<"\n";
cout<<a<<"-"<<b<<"="<<a-b<<"\n";
cout<<a<<"*"<<b<<"="<<a*b<<"\n";
cout<<a<<"/"<<b<<"="<<a/b<<"\n";
return 0;
}